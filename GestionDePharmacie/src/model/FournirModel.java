package model;

public class FournirModel {

	private int idMedFour;
	private int numFactFour;
	
	public FournirModel() {
		this.idMedFour=0;
		this.numFactFour=0;
		
	}

	public int getIdMedFour() {
		return idMedFour;
	}

	public void setIdMedFour(int idMedFour) {
		this.idMedFour = idMedFour;
	}

	public int getNumFactFour() {
		return numFactFour;
	}

	public void setNumFactFour(int numFactFour) {
		this.numFactFour = numFactFour;
	}
	
	
}
